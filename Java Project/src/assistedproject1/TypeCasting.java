package assistedproject1;

public class TypeCasting {

	public static void main(String[] args) {
		byte a = 20;
		System.out.println("value of:"+a);
		int b = a;
		System.out.println("value of:"+b);
		long l = a;
		System.out.println("value of:"+l);
		double d = a;
		System.out.println("value of:"+d);
		float f = a;
		System.out.println("value of:"+f);
		
		
		double data = 28.8;
		System.out.println("double value:"+data);
		int num =(int)data;
		System.out.println("int value:"+num);
		long p = (long)data;
		System.out.println("long value:"+p);
		float s = (float)data;
		System.out.println("float value:"+s);
		
	}
}
